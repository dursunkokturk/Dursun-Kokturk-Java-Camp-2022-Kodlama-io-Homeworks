package java2022_day1_homework_08;

public class Main {

	public static void main(String[] args) {
		// While Dongusu Ile Calismak
		int i = 1;
		
		while(i<10) {
			System.out.println(i);
			i++;
		}
		System.out.println("End Of Loop");
		
		System.out.println("");
		
		int a = 2;
		while(a<10) {
			System.out.println(a);
			a+=2;
		}
		System.out.println("End Of Loop");
	}
}