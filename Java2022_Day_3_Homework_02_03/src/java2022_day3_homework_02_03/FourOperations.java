package java2022_day3_homework_02_03;

public class FourOperations {
	
	// 2 Parametreli Fonksiyon Olusturuyoruz
	// Yapilacak Islemi Belirtiyoruz
	public int total(int number1,int number2) {
		return number1 + number2;
	}
	
	// Ilk Fonksiyon Ile Ayni Isimde Olan Bir Tane Daha 3 Parametreli Fonksiyon Olusturuyoruz
	// Yapilacak Islemi Belirtiyoruz
	public int total(int number1,int number2,int number3) {
		return number1 + number2 + number3;
	}	
}